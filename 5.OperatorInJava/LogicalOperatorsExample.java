// WAP to explain Logical Operators

public class LogicalOperatorsExample {
    public static void main(String[] args) {
        // Logical operators
        boolean x = true;
        boolean y = false;
      
        System.out.println("x && y: " + (x && y));
        System.out.println("x || y: " + (x || y));
        System.out.println("!x: " + (!x));
    }
}