// WAP to demonstrating Nested if condition

public class NestedIfConditionDemo {
    public static void main(String[] args) {
        int i = 10;

        if (i < 15) {
            System.out.println("I is smaller then 15");

            if (i == 10) {
                System.out.println("I is exactly 10");
            }
        } 
    }
}